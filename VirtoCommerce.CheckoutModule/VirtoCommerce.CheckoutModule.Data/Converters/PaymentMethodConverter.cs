using Omu.ValueInjecter;

namespace VirtoCommerce.CheckoutModule.Data.Converters
{
	public static class PaymentMethodConverter
	{
		public static Domain.Cart.Model.PaymentMethod ToCartModel(this Domain.Payment.Model.PaymentMethod paymentMethod)
		{
			var retVal = new Domain.Cart.Model.PaymentMethod();

			retVal.InjectFrom(paymentMethod);

			retVal.GatewayCode = paymentMethod.Code;
			retVal.Name = paymentMethod.Description;
			retVal.IconUrl = paymentMethod.LogoUrl;

			return retVal;
		}
	}
}
